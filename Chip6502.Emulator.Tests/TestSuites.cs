        [TestMethod]
        public void KlausDormannFunctionalTest()
        {
            var chip = TestSetup.Setup(0x400);
            TestSetup.LoadAssetCode(chip, "6502_functional_test.bin", 0x0);

            int numberOfCycles = 0;
            while (true)
            {
                chip.Step();
                numberOfCycles++;

                if (chip.State.PC == 0x3469)
                {
                    break;
                }

                if (numberOfCycles > 30646176)
                    Assert.Fail($"Maximum Number of Cycles Exceeded. At 0x{chip.State.PC:X4};");
            }

            Assert.AreEqual(0xF0, chip.State.A);

            TestSetup.TearDown(chip);
        }

